#include "ft_printf.h"

int	ft_print_nbr(int *nbr)
{
	int	i;

	i = 0;
	ft_putnbr_fd(*nbr, 1);
	if (*nbr == -2147483648)
		return (11);
	if (*nbr < 0)
	{
		*nbr *= (-1);
		i++;
	}
	while (*nbr != 0)
	{
		*nbr /= 10;
		i++;
	}
	return (i);
}